import { readFileSync, writeFileSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const SPEC_DIR = path.join(ROOT, 'spec');
const OUT = path.join(ROOT, 'client', 'spec.js');

function readJson(name) {
  return JSON.parse(readFileSync(path.join(SPEC_DIR, name), 'utf8'));
}

const resolverSource = readFileSync(path.join(SPEC_DIR, 'resolveUiMode.mjs'), 'utf8')
  .replace(/^export /gm, '')
  .replace(/export \{[^}]+\};?\s*$/m, '');

const spec = {
  phases: readJson('phases.json'),
  uiModes: readJson('ui-modes.json'),
  socketEvents: readJson('socket-events.json'),
  timing: readJson('timing.json'),
};

const output = `/* eslint-disable */
// Generated by scripts/build-client-spec.mjs — do not edit by hand.
(function (global) {
  const SPEC = ${JSON.stringify(spec, null, 2)};

  ${resolverSource}

  const PHASE_LABEL = Object.fromEntries(
    (SPEC.phases.handPhases || []).map((phase) => [phase.id, phase.label]),
  );

  global.TexasHoldemSpec = {
    SPEC,
    PHASE_LABEL,
    IN_HAND_PHASES,
    RESULT_PANEL_DELAY_MS: SPEC.timing.constants.RESULT_PANEL_DELAY_MS.default,
    SETTINGS_MODE: { EDIT: 'edit', LOCKED: 'locked', READONLY: 'readonly' },
    getHandPhaseIds: () => (SPEC.phases.handPhases || []).map((phase) => phase.id),
    getPhaseLabel: (phaseId) => getPhaseLabel(SPEC.phases, phaseId),
    isInHandPhase,
    getSettingsHandPhase,
    isSettingsEditable,
    resolveSettingsMode: (gameState, hand, options = {}) => resolveSettingsMode(
      { ...gameState, _viewerPlayerId: options.viewerPlayerId, _isHost: options.isHost },
      hand,
    ),
    resolveUiMode: (gameState, options = {}) => resolveUiMode(gameState, {
      ...options,
      phasesSpec: SPEC.phases,
      uiModesSpec: SPEC.uiModes,
    }),
  };
})(typeof window !== 'undefined' ? window : globalThis);
`;

writeFileSync(OUT, output, 'utf8');
console.log(`Wrote ${OUT}`);
